public class Position {
    private int x;
    private int y;
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
    public int setX(int x){
        this.x = x;
    }
    public int setY(int y){
        this.y = y;
    }

}
